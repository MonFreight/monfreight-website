  var STORAGE_KEY = "mflang";

  function getLang() {
    var l;
    try { l = localStorage.getItem(STORAGE_KEY); } catch (e) {}
    return (l === "en" || l === "mn") ? l : "mn";
  }

  function apply(lang) {
    var dict = T[lang] || T.mn;
    document.documentElement.lang = lang;

    document.querySelectorAll("[data-i18n]").forEach(function (el) {
      var v = dict[el.getAttribute("data-i18n")];
      if (v != null) el.innerHTML = v;
    });
    document.querySelectorAll("[data-i18n-ph]").forEach(function (el) {
      var v = dict[el.getAttribute("data-i18n-ph")];
      if (v != null) el.setAttribute("placeholder", v);
    });

    // document <title>
    var titleEl = document.querySelector("title[data-i18n]");
    if (titleEl) document.title = (dict[titleEl.getAttribute("data-i18n")] || document.title).replace(/&amp;/g, "&");

    // refresh footer year (it lives inside a translated string)
    var y = document.getElementById("year");
    if (y) y.textContent = new Date().getFullYear();

    // active state on switch buttons
    document.querySelectorAll(".lang-switch button").forEach(function (b) {
      b.classList.toggle("active", b.getAttribute("data-lang") === lang);
    });

    // open accordion panels change height when text length changes
    if (typeof window.recalcAccordions === "function") {
      window.recalcAccordions();
      setTimeout(window.recalcAccordions, 60);
    }

    try { localStorage.setItem(STORAGE_KEY, lang); } catch (e) {}
  }

  function init() {
    apply(getLang());
    document.querySelectorAll(".lang-switch button").forEach(function (b) {
      b.addEventListener("click", function () { apply(b.getAttribute("data-lang")); });
    });
  }

  if (document.readyState === "loading") {
    document.addEventListener("DOMContentLoaded", init);
  } else {
    init();
  }

  return { apply: apply, get: getLang };
